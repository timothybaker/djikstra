#include "coordinates.h"
#include <math.h>

///////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////
coordinates::coordinates()
{
    x=0.0;
    y=0.0;
}

///////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////
coordinates::coordinates(double X, double Y)
{
    x=X;
    y=Y;
}

///////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////
coordinates::~coordinates()
{
    //dtor
}

///////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////
int coordinates::find_distance(const coordinates& other)const
{
    int xdist=this->x - other.x;
    int ydist=this->y - other.y;

    int distance=sqrt((xdist*xdist)+(ydist*ydist));

    return distance;
}
